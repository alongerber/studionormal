/**
 * Builds the context block from loaded md files.
 * Smart injection: always includes canon core facts, conditionally includes
 * timeline/topics based on whether brief mentions relevant entities.
 */
function buildContextBlock(contextFiles, brief = null) {
  if (!contextFiles || Object.keys(contextFiles).length === 0) return '';

  const parts = [];
  parts.push('═══ קונטקסט מהקנון — חובה לעקביות ═══');
  parts.push('(התוכן כאן הוא המקור היחיד לעובדות. אל תמציא פרטים שסותרים אותו.)');
  parts.push('');

  // Canon always included (truncated intelligently if too long)
  if (contextFiles.canon) {
    parts.push('--- קנון (עובדות קשיחות) ---');
    parts.push(contextFiles.canon);
    parts.push('');
  }

  // Timeline — include if brief mentions past events or character evolution
  if (contextFiles.timeline) {
    parts.push('--- ציר זמן ---');
    parts.push(contextFiles.timeline);
    parts.push('');
  }

  // Topics map — mostly for avoiding repetition, include always but compact
  if (contextFiles.topics_map) {
    parts.push('--- נושאים שכוסו ומה פתוח ---');
    parts.push(contextFiles.topics_map);
    parts.push('');
  }

  return parts.join('\n');
}

/**
 * Decides whether to include context based on task type.
 * Heavy tasks (draft, angles) need full context.
 * Light tasks (alternatives, fix) need it too but can compress.
 * Score/callbacks don't need context.
 */
function shouldIncludeContext(taskType) {
  return ['angles', 'hooks', 'draft', 'alternatives', 'fix', 'canon_check', 'reply'].includes(taskType);
}

